// Take the pH level of soil and print:
// If pH < 6 → "Soil is acidic"
// If pH > 7.5 → "Soil is alkaline"
// Else → "Soil pH is neutral"


let pH = 5;

if (pH < 6){
    console.log("Soil is Acidic");
}

else if (pH > 7.5){
    console.log("Soil is Alkaline");   
}

else {
    console.log("Soil pH is neutral"); 
}