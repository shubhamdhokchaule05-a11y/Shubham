// positive or negative or zero
// Take a number and print

// "positive" if number > 0
// "negative" if number < 0
// "zero" otherwise

let number = 3;

if (number > 0)     {
    console.log(number,"positive");
}
else if (number < 0)  {
    console.log(number,"negative");
}
 else {
    console.log(number,"zero");
}